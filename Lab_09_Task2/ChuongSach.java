package Lab_09_Task2;

public class ChuongSach {
	private String tieuDe;
	private int soTrang;

	public ChuongSach(String tieuDe, int soTrang) {
		super();
		this.tieuDe = tieuDe;
		this.soTrang = soTrang;
	}

	public String getTieuDe() {
		return tieuDe;
	}

	public int getSoTrang() {
		return soTrang;
	}

}
